package electronicsMultilevel;

public class Elctronics {
	
	String power;
	String product;
	String storage;
	
	
	public Elctronics ( String power, String product, String storage) {
		this.power=power;
		this.product=product;
		this.storage=storage;
	}
	public String toString() {
		return power + product + product;
	}

}
